//Program to get and set password from user using getter setter method

package Special_Programs;

public class Person {
	// Declare instance variables as private.	
	   private String name;
	   private int age;

	// Create getter methods for each variable.
	   public String getName() {
	     return name;
	   }
	   public int getAge() {
	     return age;
	   }
	// Create setter methods for each variable.    
	   public void setName(String name) {
	     this.name = name;
	   }
	   public void setAge(int age) {
	     this.age = age;
	   }
	   
}
